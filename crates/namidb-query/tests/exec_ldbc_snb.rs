// ──────────────────────────────────────────────────────────────────────
// IC9 — Recent messages by friends-of-friends (variable-length *1..2)
// ──────────────────────────────────────────────────────────────────────

#[tokio::test]
async fn ic09_friends_of_friends_messages() {
    let mut writer = WriterSession::open(store(), paths("ic09")).await.unwrap();
    let f = build_snb_micro_graph(&mut writer).await;
    let snapshot = writer.snapshot();

    let src = read_fixture("ic09_friends_of_friends_messages.cypher");
    let q = parse(&src).expect("parse IC9");
    let plan = lower(&q).expect("lower IC9");

    let mut params = Params::new();
    params.insert("personId".into(), RuntimeValue::String(f.alice.to_string()));
    // maxDate slightly after the latest message to include all 8.
    let max_date = 1_700_000_000_000_i64 + 100 * 86_400_000;
    params.insert("maxDate".into(), RuntimeValue::Integer(max_date));

    let rows = execute(&plan, &snapshot, &params).await.expect("exec IC9");

    // Alice's KNOWS *1..2 friends (excluding Alice herself):
    //   1-hop: Bob, Carol  (alice→bob, alice→carol)
    //   2-hop: Dave (via bob→dave), Eve (via carol→eve)
    // Messages created by {Bob, Carol, Dave, Eve}: all 8 except those by
    // Frank (msg4). That gives 7 messages, but each is also reachable
    // through multiple hop counts (e.g. Bob is friend at *1 and *2 via
    // Bob→Dave→… wait, no — KNOWS *1..2 reaches him at hop 1 only).
    //
    // Care: the variable-length expand emits one row per hop that the
    // bound (min..max) admits. With *1..2 and bob reached at hop 1
    // (alice→bob), bob is matched once. dave is matched at hop 2 only
    // (alice→bob→dave). So each friend appears with the right multi-
    // plicity in the friend-list.
    //
    // BUT: friend appears in multiple paths. dave can be reached via
    // alice→bob→dave (hop 2). That's the only path of length ≤2.
    // bob can be reached at hop 1 (alice→bob). carol idem. eve via
    // carol→eve at hop 2. So each friend is matched once.
    //
    // Messages from these 4 friends: msg0 (bob), msg5 (bob), msg1 (carol),
    // msg6 (carol), msg2 (dave), msg3 (eve), msg7 (eve). Total 7 messages.
    assert!(
        !rows.is_empty(),
        "expected non-empty IC9 result, got 0 rows"
    );
    // Friends-of-friends produce the same friend node id only once per
    // hop path. Verify the result excludes msg4 (Frank's message).
    for row in &rows {
        match row.get("personId") {
            Some(RuntimeValue::String(s)) => {
                assert_ne!(
                    s,
                    &f.frank.to_string(),
                    "Frank should not appear as IC9 author"
                );
                assert_ne!(
                    s,
                    &f.alice.to_string(),
                    "Alice should be filtered by NOT friend._id = p._id"
                );
            }
            other => panic!("unexpected: {:?}", other),
        }
    }
    // Ordered DESC by creationDate then ASC by messageId — the first
    // row should be the latest qualifying message: msg7 (eve, day 7).
    match rows[0].get("messageId") {
        Some(RuntimeValue::String(s)) => {
            assert_eq!(s, &f.msg_ids[7].to_string());
        }
        other => panic!("unexpected: {:?}", other),
    }

    // Total row count: messages by {Bob, Carol, Dave, Eve} = 7. But
    // variable-length expand emits one row per (msg, hop-count) when
    // both *1 and *2 reach the same node. In this fixture each friend
    // is reachable via exactly one path within *1..2, so the count is
    // exactly 7 (we LIMIT 20 in the query so no truncation).
    assert_eq!(
        rows.len(),
        7,
        "expected 7 messages from FoF, got {} — rows: {:?}",
        rows.len(),
        rows.iter()
            .map(|r| r.get("personFirstName"))
            .collect::<Vec<_>>()
    );

    let _ = (f.bob, f.carol, f.dave, f.eve);
    let _ = f.msg_creators;
    let _ = f.comment_ids;
    let _ = f.comment_creators;
    let _ = f.comment_replies_to_msg;
}

// ──────────────────────────────────────────────────────────────────────
// IC1 — Friends with a given firstName (transitive *1..3)
// ──────────────────────────────────────────────────────────────────────

#[tokio::test]
async fn ic01_friends_by_name() {
    let mut writer = WriterSession::open(store(), paths("ic01")).await.unwrap();
    let f = build_snb_micro_graph(&mut writer).await;
    let snapshot = writer.snapshot();

    let src = read_fixture("ic01_friends_by_name.cypher");
    let q = parse(&src).expect("parse IC1");
    let plan = lower(&q).expect("lower IC1");

    let mut params = Params::new();
    params.insert("personId".into(), RuntimeValue::String(f.alice.to_string()));
    // Frank is at hop 3 from Alice (alice→bob→dave→frank or
    // alice→carol→eve→frank). Within *1..3 he is the only Person whose
    // firstName == "Frank".
    params.insert("firstName".into(), RuntimeValue::String("Frank".into()));

    let rows = execute(&plan, &snapshot, &params).await.expect("exec IC1");

    assert_eq!(rows.len(), 1, "expected exactly 1 friend, got {:?}", rows);
    match rows[0].get("firstName") {
        Some(RuntimeValue::String(s)) => assert_eq!(s, "Frank"),
        other => panic!("unexpected firstName: {:?}", other),
    }
    match rows[0].get("lastName") {
        Some(RuntimeValue::String(s)) => assert_eq!(s, "Foley"),
        other => panic!("unexpected lastName: {:?}", other),
    }
    match rows[0].get("friendId") {
        Some(RuntimeValue::String(s)) => assert_eq!(s, &f.frank.to_string()),
        other => panic!("unexpected friendId: {:?}", other),
    }
}

// ──────────────────────────────────────────────────────────────────────
// Smoke tests for the remaining LDBC IC queries (IC3-IC6, IC10-IC12).
//
// The micro graph doesn't have Country/City/Tag/Forum/Company nodes,
// so each query returns 0 rows. The point is to prove the entire
// parse → lower → optimize → execute pipeline accepts every IC*
// shape WITHOUT erroring. Correctness against a real LDBC-shaped
// dataset is validated by the bench harness (SF1/SF10 datasets).
// ──────────────────────────────────────────────────────────────────────

async fn execute_ic_smoke(name: &str, fixture: &str, person_param: NodeId) {
    let mut writer = WriterSession::open(store(), paths(name)).await.unwrap();
    build_snb_micro_graph(&mut writer).await;
    let snapshot = writer.snapshot();
    let src = read_fixture(fixture);
    let q = parse(&src).unwrap_or_else(|errs| panic!("parse {} failed: {:?}", name, errs));
    let plan = lower(&q).unwrap_or_else(|e| panic!("lower {} failed: {:?}", name, e));
    let mut params = Params::new();
    params.insert(
        "personId".into(),
        RuntimeValue::String(person_param.to_string()),
    );
    // Extra params used by various IC queries — supply sane values
    // regardless of which IC asks for them.
    params.insert("firstName".into(), RuntimeValue::String("Frank".into()));
    params.insert("countryAName".into(), RuntimeValue::String("AR".into()));
    params.insert("countryBName".into(), RuntimeValue::String("BR".into()));
    params.insert("tagName".into(), RuntimeValue::String("Music".into()));
    params.insert("tagClassName".into(), RuntimeValue::String("Person".into()));
    params.insert("countryName".into(), RuntimeValue::String("AR".into()));
    params.insert("workFromYear".into(), RuntimeValue::Integer(2020));
    params.insert("startDate".into(), RuntimeValue::Integer(0));
    params.insert("endDate".into(), RuntimeValue::Integer(2_000_000_000_000));
    params.insert("minDate".into(), RuntimeValue::Integer(0));
    let _rows = execute(&plan, &snapshot, &params)
        .await
        .unwrap_or_else(|e| panic!("execute {} failed: {:?}", name, e));
}

